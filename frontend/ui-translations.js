// Function to translate UI elements
function translateUI(lang = 'en') {
    if (!uiTranslations[lang]) lang = 'en';
    const langData = uiTranslations[lang];
    
    // Update header
    const headerTitle = document.querySelector('.header h1');
    if (headerTitle) {
        headerTitle.innerHTML = `<span class="header-emoji">💰</span> ${langData.appName}`;
    }
    
    // Update buttons by finding them through their onclick attributes
    const calcBtn = Array.from(document.querySelectorAll('.btn-secondary')).find(btn => 
        btn.getAttribute('onclick') === 'showCalculator()' || btn.textContent.includes('Calculator')
    );
    if (calcBtn) {
        const icon = calcBtn.querySelector('i');
        const emoji = calcBtn.querySelector('.btn-emoji');
        calcBtn.innerHTML = icon ? `${icon.outerHTML} <span class="btn-emoji">🧮</span> ${langData.calculator}` : langData.calculator;
    }
    
    const chartsBtn = Array.from(document.querySelectorAll('.btn-secondary')).find(btn => 
        btn.getAttribute('onclick') === 'showCharts()' || btn.textContent.includes('Live Charts')
    );
    if (chartsBtn) {
        const icon = chartsBtn.querySelector('i');
        chartsBtn.innerHTML = icon ? `${icon.outerHTML} <span class="btn-emoji">📈</span> ${langData.liveCharts}` : langData.liveCharts;
    }
    
    const newsBtn = Array.from(document.querySelectorAll('.btn-secondary')).find(btn => 
        btn.getAttribute('onclick') === 'showMarketUpdates()' || btn.textContent.includes('News')
    );
    if (newsBtn) {
        const icon = newsBtn.querySelector('i');
        newsBtn.innerHTML = icon ? `${icon.outerHTML} <span class="btn-emoji">📰</span> ${langData.news}` : langData.news;
    }
    
    const historyBtn = Array.from(document.querySelectorAll('.btn-secondary')).find(btn => 
        btn.getAttribute('onclick') === 'showHistory()' || btn.textContent.includes('History')
    );
    if (historyBtn) {
        const icon = historyBtn.querySelector('i');
        historyBtn.innerHTML = icon ? `${icon.outerHTML} <span class="btn-emoji">📜</span> ${langData.history}` : langData.history;
    }
    
    const logoutBtn = Array.from(document.querySelectorAll('.btn-danger')).find(btn => 
        btn.getAttribute('onclick') === 'logout()' || btn.textContent.includes('Logout')
    );
    if (logoutBtn) {
        const icon = logoutBtn.querySelector('i');
        logoutBtn.innerHTML = icon ? `${icon.outerHTML} ${langData.logout}` : langData.logout;
    }
    
    // Update chat title
    const chatTitle = document.querySelector('.chat-header h2');
    if (chatTitle) {
        chatTitle.innerHTML = `<span class="header-emoji">🤖</span> ${langData.chatTitle}`;
    }
    
    // Update chat input placeholder
    const chatInput = document.getElementById('chatInput');
    if (chatInput) {
        chatInput.placeholder = langData.chatPlaceholder;
    }
    
    // Update send button
    const sendBtn = document.querySelector('.chat-input-container .btn-primary');
    if (sendBtn) {
        const icon = sendBtn.querySelector('i');
        sendBtn.innerHTML = icon ? `${icon.outerHTML} <span>${langData.send}</span>` : langData.send;
    }
    
    // Update welcome message
    updateWelcomeMessage(lang);
    
    // Update quick action buttons
    updateQuickActions(lang);
    
    // Store language preference
    localStorage.setItem('uiLanguage', lang);
    if (typeof currentLanguage !== 'undefined') {
        currentLanguage = lang;
    }
}

// Update welcome message
function updateWelcomeMessage(lang) {
    if (!uiTranslations[lang]) lang = 'en';
    const langData = uiTranslations[lang];
    const welcomeDiv = document.querySelector('.message.bot-message .message-content');
    if (welcomeDiv) {
        welcomeDiv.innerHTML = `
            <p>${langData.welcomeTitle} ✨</p>
            <ul>
                <li>💡 ${langData.welcome1}</li>
                <li>🧮 ${langData.welcome2}</li>
                <li>📊 ${langData.welcome3}</li>
                <li>💰 ${langData.welcome4}</li>
                <li>📈 ${langData.welcome5}</li>
                <li>🌍 ${langData.welcome6}</li>
            </ul>
            <p>${langData.welcomeEnd} ✨</p>
        `;
    }
}

// Update quick action buttons
function updateQuickActions(lang) {
    if (!uiTranslations[lang]) lang = 'en';
    const langData = uiTranslations[lang];
    
    const quickActions = [
        { selector: '.quick-action-btn:nth-child(1)', text: langData.whatIsSip },
        { selector: '.quick-action-btn:nth-child(2)', text: langData.taxSaving },
        { selector: '.quick-action-btn:nth-child(3)', text: langData.retirement },
        { selector: '.quick-action-btn:nth-child(4)', text: langData.emergencyFund },
        { selector: '.quick-action-btn:nth-child(5)', text: langData.calculator }
    ];
    
    quickActions.forEach(action => {
        const btn = document.querySelector(action.selector);
        if (btn) {
            const emoji = btn.querySelector('.emoji-btn');
            btn.innerHTML = emoji ? `${emoji.outerHTML} ${action.text}` : action.text;
        }
    });
}

// Initialize UI language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('uiLanguage') || localStorage.getItem('preferredLanguage') || 'en';
    translateUI(savedLang);
    
    // Update language selector
    const langSelector = document.getElementById('languageSelector');
    if (langSelector) {
        langSelector.value = savedLang;
    }
});
